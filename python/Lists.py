# using a list in python
# A list is a collection of elements of different data types usually represented with a [] bracket
# example of a list

a = ["Ayo", 12.5, 290]

# Changes can be made to elements in a list using different methods e.g append,pop,insert,etc...
# Example
# a.append() # Adds a specified value to the element of a list
# a.pop()      # Remove the last item of a list
# a.remove()   # Removes the value specified within the elements
# a.sort()      # Sorts a file in an ascending order and is only relevent when elements contain only integers
# a.insert()    # inserts a given value at a specific index position
# a.reverse()   # Reverse the content of a list in descending order
for i in a:  # Displaying the contents of a list
    print(i)
